#Escreva um programa que mostre os número pares entre 1 e 20

for num in range(1, 21): 
     if num % 2 == 0: 
           print (num)

#Comentário: Neste programa foram analisados os números de 1 a 20,
#analisando a "condição" de que os números teriam que ter o resto da divisão por 2 igual a 0,
#ou seja, buscar os números pares dentre o intervalo de 1 a 20. Em seguida, o comando print
#mostra ao usuário os números pares encontrados.