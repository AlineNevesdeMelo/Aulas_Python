x = 0
while x < 10:
    print(x)
    x += 1
else:
    print("fim while")
# O while criou uma repetição em lista de todos os valores de x maior que zero e menor de 10
#o else serviu para mostrar que quando chegou no 10 não era para continuar e que operação acabou.